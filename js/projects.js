function mostrarProyecto() {
  const contenedorProyecto = document.getElementById("proyecto");

  const urlParams = new URLSearchParams(window.location.search);
  const proyectoId = urlParams.get("id");

  if (!proyectoId) {
    contenedorProyecto.innerHTML = `<p class="ProjectHeader-title"> Proyecto no proporcionado :( <br> Lo sentimos </p>`;
  }
  const proyecto = proyectos.find((p) => p.id == proyectoId);

  if (proyecto) {
    const actualId = proyectos.indexOf(proyecto);
    const prevProyecto = proyectos[actualId - 1] || proyectos[proyectos.length - 1];
    const nextProyecto = proyectos[actualId + 1] || proyectos[0];

    contenedorProyecto.innerHTML = `<article class="Project">


    <section class="ProjectHeader">
      <div class="ProjectHeader-cabecera">
        <div class="ProjectHeader-imgContainer">
          <img
            class="ProjectHeader-img"
            src= ${proyecto.icono}
            alt= ${proyecto.alt}
            loading = "lazy"
          />
        </div>
        <h2 class="ProjectHeader-title">${proyecto.titulo}</h2>
      </div>
      <p class="ProjectHeader-description">${proyecto.descripcion}
      </p>
    </section>


  <section class="SectionClave">
        <div class="Section-textContainer">
          <div class="Section-textGroup">
            <h4 class="Text-headingLead">CLAVE 1/3</h4>
            <h3 class="Text-headingMedium">${proyecto.heading1}</h3>
          </div>

          <div class="Section-parrafos">
            <p class="Text-parrafo">
             ${proyecto.text1}
            </p>
          </div>
        </div>

        <div class="Section-imgContainer">
          <img
            class="SectionClave-img"
            src="${proyecto.img1}"
            alt="${proyecto.alt1}"
            loading="lazy"
          />
        </div>
      </section>

 <section class="SectionClave SectionClave--second">
        <div class="Section-textContainer">
          <div class="Section-textGroup">
            <h4 class="Text-headingLead">CLAVE 2/3</h4>
            <h3 class="Text-headingMedium">${proyecto.heading2}</h3>
          </div>

          <div class="Section-parrafos">
            <p class="Text-parrafo">${proyecto.text2}
            </p>
          </div>
        </div>

        <div class="Section-imgContainer">
          <img
            class="SectionClave-img"
            src="${proyecto.img2}"
            alt="${proyecto.alt2}"
          />
        </div>
      </section>

      <section class="SectionClave SectionClave--third">
        <div class="Section-textContainer">
          <div class="Section-textGroup">
            <h4 class="Text-headingLead">CLAVE 3/3</h4>
            <h3 class="Text-headingMedium">${proyecto.heading3}</h3>
          </div>

          <div class="Section-parrafos">
            <p class="Text-parrafo">${proyecto.text3}
            </p>
          </div>
        </div>

        <div class="Section-imgContainer">
          <img
            class="SectionClave-img"
            src="${proyecto.img3}"
            alt="${proyecto.alt3}"
            loading="lazy"
          />
        </div>
      </section>

          <aside class="Aside" id="asideContainer">
      <a href="projects.html?id=${prevProyecto.id}" title="Visita el proyecto ${prevProyecto.titulo}" class="Aside-container" id="Aside-prev">
          <p class="Aside-text">Anterior Proyecto</p>
      </a>

      <a href="projects.html?id=${nextProyecto.id}" title="Visita el proyecto ${nextProyecto.titulo}" class="Aside-container" id="Aside-next">          <p class="Aside-text">Siguiente Proyecto</p>
      </a>
    </aside>

    </article>`;

    // Ahora obtenemos los elementos de los asides
    const asidePrev = document.getElementById("Aside-prev");
    const asideNext = document.getElementById("Aside-next");

    // Configuramos las imágenes de fondo
    if (asidePrev) {
      asidePrev.style.backgroundImage = `url(${prevProyecto.imgaside})`;
    }
    if (asideNext) {
      asideNext.style.backgroundImage = `url(${nextProyecto.imgaside})`;
    }
  } else {
    contenedorProyecto.innerHTML = `<p class="ProjectHeader-title"> Proyecto no encontrado :( <br> Lo sentimos </p>`;
  }
}

document.addEventListener("DOMContentLoaded", () => {
  mostrarProyecto();
});
